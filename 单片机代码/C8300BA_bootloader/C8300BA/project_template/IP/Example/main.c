/*********************************************************************************************************//**
  * @brief  Main program.
  * @retval None
  ***********************************************************************************************************/
int main(void)
{
		Clock_Config();
		CKCU_Configuration();               /* System Related configuration                                       */
	  LVD_Init();
		My_Ram_Init();
		Load_Clr();
		GPIO_Init();
	  Uart_Or_Usart_Init();
	  WDG_Init();
	  HT32_BFTM0_Init();
	  HT32_MCTM0_PWM_Init();
		WDT_Restart(); 
		while (1)                  
		{
				WDT_Restart();
			  Device_Buf=0;
			  if(MS_1_Sign==1)
				{
						MS_1_Sign=0;
					  Key_Scan();
					  Uart_Deal();
				}
			  Function();
				if(MS_25_Count>=40)
				{
						MS_25_Count=0;
					  //USART_SendData(UR0_WF_COM_PORT,Key);
					  USART_SendData(UR1_COM_PORT,Key);
				}
			  Systime_Updata_Function();
				Ur0_WF_Receive_Function();
				Ur1_Receive_Function();
			  Device_Function();
		}
}

/*********************************************************************************************************//**
  * @brief  Configure the NVIC vector table.
  * @retval None
  ***********************************************************************************************************/
void NVIC_Configuration(void)
{
  NVIC_SetVectorTable(NVIC_VECTTABLE_FLASH, 0x0);     /* Set the Vector Table base location at 0x00000000   */
}

